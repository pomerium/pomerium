func getFieldLocalType(fd protoreflect.FieldDescriptor) *jen.Statement {
	var s *jen.Statement
	switch fd.Kind() {
	case protoreflect.BoolKind:
		s = jen.Bool()
	case protoreflect.EnumKind:
		panic(fmt.Sprintf("enums not implemented: %s", fd.FullName()))
	case protoreflect.Int32Kind:
		s = jen.Int32()
	case protoreflect.Sint32Kind:
		s = jen.Int32()
	case protoreflect.Uint32Kind:
		s = jen.Uint32()
	case protoreflect.Int64Kind:
		s = jen.Int64()
	case protoreflect.Sint64Kind:
		s = jen.Int64()
	case protoreflect.Uint64Kind:
		s = jen.Uint64()
	case protoreflect.Sfixed32Kind:
		panic("sfixed32 not implemented")
	case protoreflect.Fixed32Kind:
		panic("fixed32 not implemented")
	case protoreflect.FloatKind:
		s = jen.Float32()
	case protoreflect.Sfixed64Kind:
		panic("sfixed64 not implemented")
	case protoreflect.Fixed64Kind:
		panic("fixed64 not implemented")
	case protoreflect.DoubleKind:
		s = jen.Float64()
	case protoreflect.StringKind:
		s = jen.String()
	case protoreflect.BytesKind:
		s = jen.Index().Byte()
	case protoreflect.MessageKind:
		switch fd.Message().FullName() {
		case "google.protobuf.BoolValue":
			s = jen.Bool()
		case "google.protobuf.Duration":
			s = jen.Qual("time", "Duration")
		case "google.protobuf.Int16Value":
			s = jen.Int16()
		case "google.protobuf.Int32Value":
			s = jen.Int32()
		case "google.protobuf.Int64Value":
			s = jen.Int64()
		case "google.protobuf.Struct":
			s = jen.Op("*").Qual("google.golang.org/protobuf/types/known/structpb", "Struct")
		case "google.protobuf.Timestamp":
			s = jen.Qual("time", "Time")
		case "google.protobuf.UInt16Value":
			s = jen.Uint16()
		case "google.protobuf.UInt32Value":
			s = jen.Uint32()
		case "google.protobuf.UInt64Value":
			s = jen.Uint64()
		case "pomerium.config.Route.StringList", "pomerium.config.Settings.StringList":
			s = jen.Index().String()
		default:
			s = jen.Id(getMessageName(fd.Message()))
		}
	case protoreflect.GroupKind:
		panic("groups not implemented")
	default:
		panic("kind not implemented")
	}
	if fd.Cardinality() == protoreflect.Repeated {
		s = jen.Index().Add(s)
	}
	s = jen.Qual("github.com/pomerium/pomerium/pkg/nullable", "Value").Index(s)
	return s
}

