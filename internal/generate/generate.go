package main

import (
	"context"
	"errors"
	"iter"
	"slices"
	"strings"

	"github.com/dave/jennifer/jen"
	"github.com/ettle/strcase"
	"github.com/rs/zerolog/log"
	"google.golang.org/protobuf/reflect/protoreflect"

	configpb "github.com/pomerium/pomerium/pkg/grpc/config"
)

var caser = strcase.NewCaser(true, map[string]bool{
	"JWT": true,
	"MCP": true,
	"PPL": true,
}, nil)

func main() {
	err := run(context.Background())
	if err != nil {
		log.Fatal().Err(err).Send()
	}
}

func run(ctx context.Context) error {
	return generateConfig(ctx)
}

func generateConfig(ctx context.Context) error {
	f := jen.NewFile("config")
	f.PackageComment("// Code generated by internal/generate DO NOT EDIT.")

	mds := []protoreflect.MessageDescriptor{
		new(configpb.BlobStorageSettings).ProtoReflect().Descriptor(),
		new(configpb.Route).ProtoReflect().Descriptor(),
		new(configpb.Settings).ProtoReflect().Descriptor(),
	}
	err := errors.Join(
		generateConfigTypes(ctx, f.Group, mds),
		generateConfigFromProtoFuncs(ctx, f.Group, mds),
		generateConfigToProtoFuncs(ctx, f.Group, mds),
	)
	if err != nil {
		return err
	}
	return f.Save("./config/config.gen.go")
}

func generateConfigFromProtoFuncs(_ context.Context, g *jen.Group, mds []protoreflect.MessageDescriptor) error {
	for _, md := range mds {
		g.Func().
			Id("convert"+getLocalMessageName(md)+"FromProto").
			Params(
				jen.Id("dst").Op("*").Id(getLocalMessageName(md)),
				jen.Id("src").Op("*").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
			).
			Params(jen.Id("error")).
			BlockFunc(func(g *jen.Group) {
				g.If(jen.Id("src").Op("==").Nil()).BlockFunc(func(g *jen.Group) {
					g.Return(jen.Nil())
				})
				g.Return().Qual("errors", "Join").CallFunc(func(g *jen.Group) {
					for fd := range iterateMessageFields(md) {
						protoName := string(fd.Name())
						goName := caser.ToPascal(protoName)
						protoGoName := strcase.ToPascal(protoName)
						g.Line().Id("convert"+getFieldTypeName(fd)+"FromProto").Call(
							jen.Op("&").Id("dst").Dot(goName),
							jen.Id("src").Dot(protoGoName),
						)
					}
					g.Line()
				})
			})
		g.Line()
		g.Func().
			Id("convertOptional"+getLocalMessageName(md)+"FromProto").
			Params(
				jen.Id("dst").Op("**").Id(getLocalMessageName(md)),
				jen.Id("src").Op("*").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
			).
			Params(jen.Id("error")).
			BlockFunc(func(g *jen.Group) {
				g.If(jen.Id("src").Op("==").Nil()).BlockFunc(func(g *jen.Group) {
					g.Return(jen.Nil())
				})
				g.Op("*").Id("dst").Op("=").New(jen.Id(getLocalMessageName(md)))
				g.Return().Qual("errors", "Join").CallFunc(func(g *jen.Group) {
					for fd := range iterateMessageFields(md) {
						protoName := string(fd.Name())
						goName := caser.ToPascal(protoName)
						protoGoName := strcase.ToPascal(protoName)
						g.Line().Id("convert"+getFieldTypeName(fd)+"FromProto").Call(
							jen.Op("&").Parens(jen.Op("*").Id("dst")).Dot(goName),
							jen.Id("src").Dot(protoGoName),
						)
					}
					g.Line()
				})
			})
		g.Line()
	}
	return nil
}

