func getMessageName(md protoreflect.MessageDescriptor) string {
	msgName := string(md.FullName())
	msgName = strings.TrimPrefix(msgName, "pomerium.config.")
	msgName = strings.TrimPrefix(msgName, "google.protobuf.")
	msgName = strings.ReplaceAll(msgName, ".", "_")
	return msgName
}

func getLocalMessageName(md protoreflect.MessageDescriptor) string {
	name := getMessageName(md)
	switch name {
	case "Route":
		return "RouteOptions"
	case "Settings":
		return "GlobalOptions"
	}
	return name
}

func isPrimitiveOptional(fd protoreflect.FieldDescriptor) bool {
	return fd.HasOptionalKeyword() && fd.Kind() != protoreflect.MessageKind
}

func iterateMessageFields(md protoreflect.MessageDescriptor) iter.Seq[protoreflect.FieldDescriptor] {
	fds := md.Fields()
	var s []protoreflect.FieldDescriptor
	for i := range fds.Len() {
		fd := fds.Get(i)
		if md.FullName() == "pomerium.config.Route" && fd.Number() < 93 {
			continue
		}
		if md.FullName() == "pomerium.config.Settings" && fd.Number() < 180 {
			continue
		}
		s = append(s, fd)
	}
	slices.SortFunc(s, func(a, b protoreflect.FieldDescriptor) int {
		return strings.Compare(string(a.FullName()), string(b.FullName()))
	})
	return slices.Values(s)
}

func toGoPascalCase(str string) string {
	r := strings.NewReplacer(
		"Uri", "URI",
		"Url", "URL",
		"Id", "ID",
		"Ppl", "PPL",
		"Mcp", "MCP",
		"Ssh", "SSH",
		"CaKey", "CAKey",
		"Jwt", "JWT",
		"Grpc", "GRPC",
		"Dns", "DNS",
		"5xx", "_5xx",
	)
	return r.Replace(strcase.ToPascal(str))
}

func toProtoPascalCase(str string) string {
	r := strings.NewReplacer(
		"5xx", "_5Xx",
	)
	return r.Replace(strcase.ToPascal(str))
}
