func getMessageName(md protoreflect.MessageDescriptor) string {
	msgName := string(md.FullName())
	msgName = strings.TrimPrefix(msgName, "pomerium.config.")
	msgName = strings.ReplaceAll(msgName, ".", "")
	return msgName
}

func iterateMessageFields(md protoreflect.MessageDescriptor) iter.Seq[protoreflect.FieldDescriptor] {
	fds := md.Fields()
	var s []protoreflect.FieldDescriptor
	for i := range fds.Len() {
		fd := fds.Get(i)
		if md.FullName() == "pomerium.config.Route" && fd.Number() < 93 {
			continue
		}
		if md.FullName() == "pomerium.config.Settings" && fd.Number() < 180 {
			continue
		}
		s = append(s, fd)
	}
	slices.SortFunc(s, func(a, b protoreflect.FieldDescriptor) int {
		return strings.Compare(string(a.FullName()), string(b.FullName()))
	})
	return slices.Values(s)
}
