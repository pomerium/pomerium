func generateConfigTypes(_ context.Context, g *jen.Group, mds []protoreflect.MessageDescriptor) error {
	for _, md := range mds {
		g.Type().Id(getLocalMessageName(md)).StructFunc(func(g *jen.Group) {
			for fd := range iterateMessageFields(md) {
				protoName := string(fd.Name())
				goName := toGoPascalCase(protoName)
				g.Id(goName).
					Add(getFieldLocalType(fd)).
					Tag(map[string]string{
						"mapstructure": protoName,
						"json":         protoName + ",omitzero",
						"yaml":         protoName + ",omitempty",
					})
			}
		})
		g.Line()
	}
	return nil
}

func generateConfigFromProtoFuncs(_ context.Context, g *jen.Group, mds []protoreflect.MessageDescriptor) error {
	for _, md := range mds {
		g.Func().Id("setNullable"+getLocalMessageName(md)+"FromProto").
			Params(
				jen.Id("dst").Op("*").Qual("github.com/pomerium/pomerium/pkg/nullable", "Value").Index(jen.Id(getLocalMessageName(md))),
				jen.Id("src").Op("*").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
			).
			Error().
			BlockFunc(func(g *jen.Group) {
				g.If(jen.Id("src").Op("==").Nil()).BlockFunc(func(g *jen.Group) {
					g.Return(jen.Nil())
				})
				g.Id("obj").Op(":=").Id("dst").Dot("Value")
				g.Id("err").Op(":=").Id("set"+getLocalMessageName(md)+"FromProto").Call(jen.Op("&").Id("obj"), jen.Id("src"))
				g.If(jen.Id("err").Op("==").Nil()).Block(jen.Return().Id("err"))
				g.Op("*").Id("dst").Op("=").Qual("github.com/pomerium/pomerium/pkg/nullable", "From").Call(
					jen.Id("obj"),
				)
				g.Return().Nil()
			})
		g.Line()

		g.Func().Id("setNullableSliceOf"+getLocalMessageName(md)+"FromProto").
			Params(
				jen.Id("dst").Op("*").Qual("github.com/pomerium/pomerium/pkg/nullable", "Value").Index(jen.Index().Id(getLocalMessageName(md))),
				jen.Id("src").Index().Op("*").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
			).
			Error().
			BlockFunc(func(g *jen.Group) {
				g.If(jen.Id("src").Op("==").Nil()).BlockFunc(func(g *jen.Group) {
					g.Return(jen.Nil())
				})
				g.Id("obj").Op(":=").Id("make").Call(jen.Index().Id(getLocalMessageName(md)), jen.Len(jen.Id("src")))
				g.For(jen.Id("i").Op(":=").Range().Id("src")).BlockFunc(func(g *jen.Group) {
					g.Id("err").Op(":=").Id("set"+getLocalMessageName(md)+"FromProto").Call(
						jen.Op("&").Id("obj").Index(jen.Id("i")),
						jen.Id("src").Index(jen.Id("i")),
					)
					g.If(jen.Id("err").Op("!=").Nil()).BlockFunc(func(g *jen.Group) {
						g.Return().Id("err")
					})
				})
				g.Op("*").Id("dst").Op("=").Qual("github.com/pomerium/pomerium/pkg/nullable", "From").Call(
					jen.Id("obj"),
				)
				g.Return().Nil()
			})
		g.Line()

		g.Func().Id("set"+getLocalMessageName(md)+"FromProto").
			Params(
				jen.Id("dst").Op("*").Id(getLocalMessageName(md)),
				jen.Id("src").Op("*").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
			).
			Error().
			BlockFunc(func(g *jen.Group) {
				g.If(jen.Id("src").Op("==").Nil()).BlockFunc(func(g *jen.Group) {
					g.Return(jen.Nil())
				})
				g.Return().Qual("errors", "Join").CallFunc(func(g *jen.Group) {
					for fd := range iterateMessageFields(md) {
						protoName := string(fd.Name())
						goName := toGoPascalCase(protoName)
						protoGoName := toProtoPascalCase(protoName)
						setterName := getFieldTypeName(fd)
						if fd.Cardinality() == protoreflect.Repeated {
							setterName = "SliceOf" + setterName
						}
						srcRef := jen.Id("src").Dot(protoGoName)
						if fd.HasOptionalKeyword() {
							// do nothing
						} else if od := fd.ContainingOneof(); od != nil {
							srcRef = jen.Id("getOneOf").Call(
								jen.Id("src").Dot(toProtoPascalCase(string(od.Name()))),
								jen.New(jen.Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)+"_"+protoGoName)),
								jen.Id("src").Dot("Get"+protoGoName).Call(),
							)
						} else if fd.Kind() == protoreflect.MessageKind {
							// do nothing
						} else {
							srcRef = jen.Op("&").Add(srcRef)
						}
						g.Line().Id("setNullable"+setterName+"FromProto").Call(
							jen.Op("&").Id("dst").Dot(goName),
							srcRef,
						)
					}
					g.Line()
				})
			})
		g.Line()
	}
	return nil
}

func generateConfigToProtoFuncs(_ context.Context, g *jen.Group, mds []protoreflect.MessageDescriptor) error {
	for _, md := range mds {
		g.Func().Id("setNullable"+getLocalMessageName(md)+"ToProto").
			Params(
				jen.Id("dst").Op("**").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
				jen.Id("src").Qual("github.com/pomerium/pomerium/pkg/nullable", "Value").Index(jen.Id(getLocalMessageName(md))),
			).
			Error().
			BlockFunc(func(g *jen.Group) {
				g.If(jen.Op("!").Id("src").Dot("IsSet")).BlockFunc(func(g *jen.Group) {
					g.Return(jen.Nil())
				})
				g.Return().Id("set"+getLocalMessageName(md)+"ToProto").Call(
					jen.Id("dst"),
					jen.New(jen.Id("src").Dot("Value")),
				)
			})
		g.Line()

		g.Func().Id("setNullableSliceOf"+getLocalMessageName(md)+"ToProto").
			Params(
				jen.Id("dst").Op("*").Index().Op("*").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
				jen.Id("src").Qual("github.com/pomerium/pomerium/pkg/nullable", "Value").Index(jen.Index().Id(getLocalMessageName(md))),
			).
			Error().
			BlockFunc(func(g *jen.Group) {
				g.If(jen.Op("!").Id("src").Dot("IsSet")).BlockFunc(func(g *jen.Group) {
					g.Return(jen.Nil())
				})
				g.Id("obj").Op(":=").Id("make").Call(
					jen.Index().Op("*").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
					jen.Len(jen.Id("src").Dot("Value")),
				)
				g.For(jen.Id("i").Op(":=").Range().Id("src").Dot("Value")).BlockFunc(func(g *jen.Group) {
					g.Id("err").Op(":=").Id("set"+getLocalMessageName(md)+"ToProto").Call(
						jen.Op("&").Id("obj").Index(jen.Id("i")),
						jen.New(jen.Id("src").Dot("Value").Index(jen.Id("i"))),
					)
					g.If(jen.Id("err").Op("!=").Nil()).BlockFunc(func(g *jen.Group) {
						g.Return().Id("err")
					})
				})
				g.Op("*").Id("dst").Op("=").Id("obj")
				g.Return().Nil()
			})
		g.Line()

		g.Func().
			Id("set"+getLocalMessageName(md)+"ToProto").
			Params(
				jen.Id("dst").Op("**").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)),
				jen.Id("src").Op("*").Id(getLocalMessageName(md)),
			).
			Error().
			BlockFunc(func(g *jen.Group) {
				g.If(jen.Id("src").Op("==").Nil()).BlockFunc(func(g *jen.Group) {
					g.Return(jen.Nil())
				})
				g.If(jen.Op("*").Id("dst").Op("==").Nil()).BlockFunc(func(g *jen.Group) {
					g.Op("*").Id("dst").Op("=").New(jen.Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)))
				})
				g.Id("obj").Op(":=").Op("*").Id("dst")
				g.Return().Qual("errors", "Join").CallFunc(func(g *jen.Group) {
					for fd := range iterateMessageFields(md) {
						protoName := string(fd.Name())
						goName := toGoPascalCase(protoName)
						protoGoName := toProtoPascalCase(protoName)
						setterName := getFieldTypeName(fd)
						if fd.Cardinality() == protoreflect.Repeated {
							setterName = "SliceOf" + setterName
						}
						if fd.HasOptionalKeyword() {
							g.Line().Id("setNullable"+setterName+"ToProto").Call(
								jen.Op("&").Id("obj").Dot(protoGoName),
								jen.Id("src").Dot(goName),
							)
						} else if od := fd.ContainingOneof(); od != nil {
							g.Line().Id("setOneOf").Call(
								jen.Op("&").Id("obj").Dot(toProtoPascalCase(string(od.Name()))),
								jen.Op("&").Qual("github.com/pomerium/pomerium/pkg/grpc/config", getMessageName(md)+"_"+protoGoName).Values(
									jen.Id(protoGoName).Op(":").Id("src").Dot(goName).Dot("Value"),
								),
								jen.Id("src").Dot(goName),
							)
						} else if fd.Kind() == protoreflect.MessageKind {
							g.Line().Id("setNullable"+setterName+"ToProto").Call(
								jen.Op("&").Id("obj").Dot(protoGoName),
								jen.Id("src").Dot(goName),
							)
						} else {
							g.Line().Id("setNullable"+setterName+"ToProto").Call(
								jen.New(jen.Op("&").Id("obj").Dot(protoGoName)),
								jen.Id("src").Dot(goName),
							)
						}
					}
					g.Line()
				})
			})
		g.Line()
	}
	return nil
}

